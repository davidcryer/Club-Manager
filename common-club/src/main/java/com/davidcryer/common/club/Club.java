package com.davidcryer.common.club;

public class Club {
    private long id;
    private String name;

    public Club() {

    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
